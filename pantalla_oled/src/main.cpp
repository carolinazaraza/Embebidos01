void setup() {
  // Inicializar la comunicaci√≥n I2C en los pines 21 (SDA) y 22 (SCL)
  Wire.begin(21, 22);
  // Inicializar la pantalla OLED
  u8g2.begin();
}

void loop() {
  // Limpiar el buffer
  u8g2.clearBuffer();
  // Dibujar la imagen en la pantalla
  u8g2.drawXBM(0, 0, 128, 64, mickey_bits);
  // Enviar el buffer a la pantalla
  u8g2.sendBuffer();
  // Esperar un segundo
  delay(1000);
}